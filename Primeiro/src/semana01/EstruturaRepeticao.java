package semana01;

import java.util.Scanner;

public class EstruturaRepeticao {
    public static void main(String[] args) {

        for ( int i = 0; i < 3; i++) {
            System.out.println("Valor de i: " + i);
        }

        int n = 0;
        while(n < 5) {
            System.out.println("Valor de n: " + n);
            n++;
        }


    }
}
